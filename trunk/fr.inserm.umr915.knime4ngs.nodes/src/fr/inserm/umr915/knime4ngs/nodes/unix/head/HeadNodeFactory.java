package fr.inserm.umr915.knime4ngs.nodes.unix.head;

import org.knime.core.node.NodeDialogPane;
import org.knime.core.node.NodeFactory;
import org.knime.core.node.NodeView;


/**
 * <code>NodeFactory</code> for the "VCFSource" Node.
 * Reads a VCF file
 *
 * @author Pierre Lindenbaum
 */
public class HeadNodeFactory
        extends NodeFactory<HeadNodeModel> {

    /**
     * {@inheritDoc}
     */
    @Override
    public HeadNodeModel createNodeModel() {
        return new HeadNodeModel();
    }

    /**
     * {@inheritDoc}
     */
    @Override
    public int getNrNodeViews() {
        return 0;
    }

    /**
     * {@inheritDoc}
     */
    @Override
    public NodeView<HeadNodeModel> createNodeView(final int viewIndex,
            final HeadNodeModel nodeModel) {
        throw new IllegalStateException();
    }

    /**
     * {@inheritDoc}
     */
    @Override
    public boolean hasDialog() {
        return true;
    }

    /**
     * {@inheritDoc}
     */
    @Override
    public NodeDialogPane createNodeDialogPane() {
        return new HeadNodeDialog();
    }

}

