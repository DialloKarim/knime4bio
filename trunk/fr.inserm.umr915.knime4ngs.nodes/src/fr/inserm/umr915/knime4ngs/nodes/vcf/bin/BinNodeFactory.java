package fr.inserm.umr915.knime4ngs.nodes.vcf.bin;

import org.knime.core.node.NodeDialogPane;
import org.knime.core.node.NodeFactory;
import org.knime.core.node.NodeView;


/**
 * <code>NodeFactory</code> for the "VCFSource" Node.
 * Reads a VCF file
 *
 * @author Pierre Lindenbaum
 */
public class BinNodeFactory
        extends NodeFactory<BinNodeModel> {

    /**
     * {@inheritDoc}
     */
    @Override
    public BinNodeModel createNodeModel() {
        return new BinNodeModel();
    }

    /**
     * {@inheritDoc}
     */
    @Override
    public int getNrNodeViews() {
        return 0;
    }

    /**
     * {@inheritDoc}
     */
    @Override
    public NodeView<BinNodeModel> createNodeView(final int viewIndex,
            final BinNodeModel nodeModel) {
        throw new IllegalStateException();
    }

    /**
     * {@inheritDoc}
     */
    @Override
    public boolean hasDialog() {
        return true;
    }

    /**
     * {@inheritDoc}
     */
    @Override
    public NodeDialogPane createNodeDialogPane() {
        return new BinNodeDialog();
    }

}

