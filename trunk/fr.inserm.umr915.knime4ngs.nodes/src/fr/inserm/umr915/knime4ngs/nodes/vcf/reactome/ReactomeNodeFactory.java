package fr.inserm.umr915.knime4ngs.nodes.vcf.reactome;

import org.knime.core.node.NodeDialogPane;
import org.knime.core.node.NodeFactory;
import org.knime.core.node.NodeView;


/**
 * <code>NodeFactory</code> for the "VCFSource" Node.
 * Reads a VCF file
 *
 * @author Pierre Lindenbaum
 */
public class ReactomeNodeFactory
        extends NodeFactory<ReactomeNodeModel> {

    /**
     * {@inheritDoc}
     */
    @Override
    public ReactomeNodeModel createNodeModel() {
        return new ReactomeNodeModel();
    }

    /**
     * {@inheritDoc}
     */
    @Override
    public int getNrNodeViews() {
        return 0;
    }

    /**
     * {@inheritDoc}
     */
    @Override
    public NodeView<ReactomeNodeModel> createNodeView(final int viewIndex,
            final ReactomeNodeModel nodeModel) {
        throw new IllegalStateException();
    }

    /**
     * {@inheritDoc}
     */
    @Override
    public boolean hasDialog() {
        return true;
    }

    /**
     * {@inheritDoc}
     */
    @Override
    public NodeDialogPane createNodeDialogPane() {
        return new ReactomeNodeDialog();
    }

}

