package fr.inserm.umr915.knime4ngs.nodes.vcf.sum;

import org.knime.core.node.NodeDialogPane;
import org.knime.core.node.NodeFactory;
import org.knime.core.node.NodeView;


/**
 * <code>NodeFactory</code> for the "VCFSource" Node.
 * Reads a VCF file
 *
 * @author Pierre Lindenbaum
 */
public class SumNodeFactory
        extends NodeFactory<SumNodeModel> {

    /**
     * {@inheritDoc}
     */
    @Override
    public SumNodeModel createNodeModel() {
        return new SumNodeModel();
    }

    /**
     * {@inheritDoc}
     */
    @Override
    public int getNrNodeViews() {
        return 0;
    }

    /**
     * {@inheritDoc}
     */
    @Override
    public NodeView<SumNodeModel> createNodeView(final int viewIndex,
            final SumNodeModel nodeModel) {
        throw new IllegalStateException();
    }

    /**
     * {@inheritDoc}
     */
    @Override
    public boolean hasDialog() {
        return true;
    }

    /**
     * {@inheritDoc}
     */
    @Override
    public NodeDialogPane createNodeDialogPane() {
        return new SumNodeDialog();
    }

}

