package fr.inserm.umr915.knime4ngs.nodes.vcf.evs;

import org.knime.core.node.NodeDialogPane;
import org.knime.core.node.NodeFactory;
import org.knime.core.node.NodeView;


/**
 * <code>NodeFactory</code> for the "VCFSource" Node.
 * Reads a VCF file
 *
 * @author Pierre Lindenbaum
 */
public class EVSNodeFactory
        extends NodeFactory<EVSNodeModel> {

    /**
     * {@inheritDoc}
     */
    @Override
    public EVSNodeModel createNodeModel() {
        return new EVSNodeModel();
    }

    /**
     * {@inheritDoc}
     */
    @Override
    public int getNrNodeViews() {
        return 0;
    }

    /**
     * {@inheritDoc}
     */
    @Override
    public NodeView<EVSNodeModel> createNodeView(final int viewIndex,
            final EVSNodeModel nodeModel) {
        throw new IllegalStateException();
    }

    /**
     * {@inheritDoc}
     */
    @Override
    public boolean hasDialog() {
        return true;
    }

    /**
     * {@inheritDoc}
     */
    @Override
    public NodeDialogPane createNodeDialogPane() {
        return new EVSNodeDialog();
    }

}

