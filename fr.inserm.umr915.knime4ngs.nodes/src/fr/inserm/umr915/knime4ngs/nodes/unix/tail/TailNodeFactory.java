package fr.inserm.umr915.knime4ngs.nodes.unix.tail;

import org.knime.core.node.NodeDialogPane;
import org.knime.core.node.NodeFactory;
import org.knime.core.node.NodeView;


/**
 * <code>NodeFactory</code> for the "VCFSource" Node.
 * Reads a VCF file
 *
 * @author Pierre Lindenbaum
 */
public class TailNodeFactory
        extends NodeFactory<TailNodeModel> {

    /**
     * {@inheritDoc}
     */
    @Override
    public TailNodeModel createNodeModel() {
        return new TailNodeModel();
    }

    /**
     * {@inheritDoc}
     */
    @Override
    public int getNrNodeViews() {
        return 0;
    }

    /**
     * {@inheritDoc}
     */
    @Override
    public NodeView<TailNodeModel> createNodeView(final int viewIndex,
            final TailNodeModel nodeModel) {
        throw new IllegalStateException();
    }

    /**
     * {@inheritDoc}
     */
    @Override
    public boolean hasDialog() {
        return true;
    }

    /**
     * {@inheritDoc}
     */
    @Override
    public NodeDialogPane createNodeDialogPane() {
        return new TailNodeDialog();
    }

}

