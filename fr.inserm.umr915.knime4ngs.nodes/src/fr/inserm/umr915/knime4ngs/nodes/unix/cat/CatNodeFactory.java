package fr.inserm.umr915.knime4ngs.nodes.unix.cat;

import org.knime.core.node.NodeDialogPane;
import org.knime.core.node.NodeFactory;
import org.knime.core.node.NodeView;


/**
 * <code>NodeFactory</code> for the "VCFSource" Node.
 * Reads a VCF file
 *
 * @author Pierre Lindenbaum
 */
public class CatNodeFactory
        extends NodeFactory<CatNodeModel> {

    /**
     * {@inheritDoc}
     */
    @Override
    public CatNodeModel createNodeModel() {
        return new CatNodeModel();
    }

    /**
     * {@inheritDoc}
     */
    @Override
    public int getNrNodeViews() {
        return 0;
    }

    /**
     * {@inheritDoc}
     */
    @Override
    public NodeView<CatNodeModel> createNodeView(final int viewIndex,
            final CatNodeModel nodeModel) {
        throw new IllegalStateException();
    }

    /**
     * {@inheritDoc}
     */
    @Override
    public boolean hasDialog() {
        return true;
    }

    /**
     * {@inheritDoc}
     */
    @Override
    public NodeDialogPane createNodeDialogPane() {
        return new CatNodeDialog();
    }

}

