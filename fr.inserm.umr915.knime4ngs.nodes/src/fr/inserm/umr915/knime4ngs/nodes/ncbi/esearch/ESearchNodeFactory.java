package fr.inserm.umr915.knime4ngs.nodes.ncbi.esearch;

import org.knime.core.node.NodeDialogPane;
import org.knime.core.node.NodeFactory;
import org.knime.core.node.NodeView;


/**
 * <code>NodeFactory</code> for the "VCFSource" Node.
 * Reads a VCF file
 *
 * @author Pierre Lindenbaum
 */
public class ESearchNodeFactory
        extends NodeFactory<ESearchNodeModel> {

    /**
     * {@inheritDoc}
     */
    @Override
    public ESearchNodeModel createNodeModel() {
        return new ESearchNodeModel();
    }

    /**
     * {@inheritDoc}
     */
    @Override
    public int getNrNodeViews() {
        return 0;
    }

    /**
     * {@inheritDoc}
     */
    @Override
    public NodeView<ESearchNodeModel> createNodeView(final int viewIndex,
            final ESearchNodeModel nodeModel) {
        throw new IllegalStateException();
    }

    /**
     * {@inheritDoc}
     */
    @Override
    public boolean hasDialog() {
        return true;
    }

    /**
     * {@inheritDoc}
     */
    @Override
    public NodeDialogPane createNodeDialogPane() {
        return new ESearchNodeDialog();
    }

}

